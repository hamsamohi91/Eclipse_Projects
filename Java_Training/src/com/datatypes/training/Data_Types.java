package com.datatypes.training;

public class Data_Types {

	public static void main(String[] args) {

		byte type_1 = 10;

		System.out.println("Type 1 = " + type_1);

		short type_2 = 20000;
		System.out.println("Type 2 = " + type_2);

		int type_3 = 30000000;
		System.out.println("Type 3 = " + type_3);

		long type_4 = 4000000000000l;
		System.out.println("Type 4 = " + type_4);

		System.out.println(type_1);
		System.out.println(type_2);
		System.out.println(type_3);
		System.out.println(type_4);

	}
}
