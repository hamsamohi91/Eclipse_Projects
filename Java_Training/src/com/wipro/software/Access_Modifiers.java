package com.wipro.software;

public class Access_Modifiers {

	private void modifier_1() {

		System.out.println("Modifier 1 = Private");
	}

	public void modifier_2() {
		System.out.println("Modifier 2 = Public");
	}

	protected void modifier_3() {
		System.out.println("Modifier 3 = Protected");

	}

	void modifier_4() {
		System.out.println("Modifier 4 = Default");

	}

}
