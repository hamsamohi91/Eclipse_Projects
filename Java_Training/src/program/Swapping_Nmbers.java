package program;

public class Swapping_Nmbers {

	public static void main(String[] args) {
		int a = 67;
		
		int b = 112;
		
		System.out.println("Before Swapping");
System.out.println("a = "+a + " and b = "+b);	

b = b+a; //20+10=30
a=b-a; //30-10=20
b=b-a; //30-20=10

System.out.println("After Swapping");
System.out.println("a = "+a + " and b = "+b);	









	
	
	}
	
	
}
